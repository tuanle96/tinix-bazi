/**
 * Hàm lấy tooltip cho một thuật ngữ
 */
export const getTooltip = (term) => {
    if (!term) return null;

    // Tìm chính xác
    if (allTerms[term]) return allTerms[term];

    // Tìm không phân biệt hoa thường
    const lowerTerm = term.toLowerCase();
    for (const [key, value] of Object.entries(allTerms)) {
        if (key.toLowerCase() === lowerTerm) return value;
    }

    // Tìm chứa từ khóa
    for (const [key, value] of Object.entries(allTerms)) {
        if (term.includes(key) || key.includes(term)) return value;
    }

    return null;
};

export default allTerms;
